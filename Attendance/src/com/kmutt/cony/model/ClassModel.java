package com.kmutt.cony.model;

public class ClassModel {
	private String mClassName;
	private String mClassId;
	private String mClassDescription;

	public String getClassName() {
		return mClassName;
	}

	public void setClassName(String className) {
		this.mClassName = className;
	}

	public String getClassId() {
		return mClassId;
	}

	public void setClassId(String mClassId) {
		this.mClassId = mClassId;
	}

	public String getClassDescription() {
		return mClassDescription;
	}

	public void setClassDescription(String mClassDescription) {
		this.mClassDescription = mClassDescription;
	}
	
}
